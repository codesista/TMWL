			      <strong>Meet Mama Lucy</strong>.<i>She sold chickens &amp; turned her income into an education for hundreds.</i>
			    </div>
			    <div id="cm_main">
			    	<p>My village in Arusha, Tanzania did not have many good schools. So, I started a primary school in 2003 with money I raised from a small chicken farm.  I began with only 10 students.  Now I serve more than 411 kids and my school is currently ranked #2 in my district out of 123 schools.  
					</p>
					
					<p>I am Mama Lucy Kamptoni.</p>

					<p>Over the past two years, TweetsGiving and Epic Change have supported the expansion of our school, and our primary school is now complete - with seven classrooms, even a school bus, library and technology lab.  Most importantly, our students continue to perform well, and have been recognized as some of the best and brightest in our area.  While I'm excited about our progress, I'm also honestly worried about what will happen to these children once they leave the doors of our primary school.</p>

				    <p>
				    	Primary education in Tanzania ends at class 7. Next year 2011 October, our first class 7 will sit for their final primary exams. Thereafter, they'll need a good secondary school to join by January 2012. We believe they'll perform well enough to earn a place in secondary school, but due to the lack of enough good schools in our country, the Government will place them to any available school. What I know is that, most of the government schools across the country lack good/enough teachers and also teaching materials. I've come across secondary school students complaining on going to school for almost a month without being taught one/two subjects like Math or Science. I'm so much worried about this for my students. 
				    </p> 
			      <p>
			      	How can <a href="http://twitter.com/gideon_gidori" target="_blank">Gideon</a> fulfill his dream of becoming an astronaut without ongoing good education after the primary level? What about <a href="http://twitter.com/glory_abraham" target="_blank">Glory</a> who's dream is to be a lawyer and stand for human rights? Will <a href="http://twitter.com/leah_albert" target="_blank">Leah's</a> dream of being a doctor come true without continuing good education? These children have incredible potential to transform our nation.  It's my hope that we can continue their education until they become independent young adults ready to lead Tanzania.  
			      </p>
			      <p>I ask you to join us on making it possible. The world we share will be better for all of us if these children are able to reach their amazing potential.  I know that's true.</p>
			      <p>I'm so grateful for what many people across the globe have done so far for my dream and for these kids. <a href="http://www.epicchangeblog.org/wedidit" target="_blank">Your shared love and gratitude made so much possible.</a> Thank you so much for pushing these kids' dreams into reality!</p>
			      
